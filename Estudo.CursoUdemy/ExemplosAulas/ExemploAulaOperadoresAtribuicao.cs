using System;

namespace Estudo.CursoUdemy.RecapLogica.ExemplosAulas
{
    public class ExemploAulaOperadoresAtribucao
    {
        public static void exemplo()
        {

            int a = 10;
            Console.WriteLine(a);

            a += 2;
            Console.WriteLine(a);

            a *= 3;
            Console.WriteLine(a);

            a /= 2;
            Console.WriteLine(a);

            string t = "A";
            Console.WriteLine(t);

            t += "B";
            Console.WriteLine(t);

            t += "C";
            Console.WriteLine(t);

            a++;

        }
    }
}